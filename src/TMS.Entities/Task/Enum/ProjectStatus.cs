using System.ComponentModel;

namespace TMS.Entities.Task.Enum;

public enum ProjectStatus
{
    /// <summary>
    /// Проект создан, но не опубликован.
    /// Доступен только владельцу.
    /// Можно вносить изменения без ограничений.
    /// </summary>
    [Description("Черновик")]
    Draft = 1,

    /// <summary>
    /// Проект отправлен на одобрение.
    /// Ожидает проверки модератором/руководством.
    /// Доступ ограничен.
    /// </summary>
    [Description("На рассмотрении")]
    Pending = 2,

    /// <summary>
    /// Проект запущен.
    /// Доступны все функции.
    /// Задачи можно создавать и выполнять.
    /// </summary>
    [Description("Активен")]
    Active = 3,

    /// <summary>
    /// Временная приостановка работы.
    /// Задачи не создаются.
    /// Существующие задачи остаются в текущем состоянии.
    /// </summary>
    [Description("Приостановлен")]
    OnHold = 4,

    /// <summary>
    /// Все задачи выполнены.
    /// Проект закрыт.
    /// Доступ только для просмотра.
    /// </summary>
    [Description("Завершен")]
    Completed = 5,

    /// <summary>
    /// Проект прекращен.
    /// Задачи аннулированы.
    /// Доступ ограничен.
    /// </summary>
    [Description("Отменен")]
    Cancelled = 6,

    /// <summary>
    /// Проект перемещен в архив.
    /// Доступен только для истории.
    /// Нет активной работы.
    /// </summary>
    [Description("Архив")]
    Archived = 7,

    /// <summary>
    /// Проект требует ревизии.
    /// Возможна корректировка.
    /// </summary>
    [Description("На проверке")]
    Review = 8,

    /// <summary>
    /// Этап формирования плана.
    /// Определение целей и задач.
    /// </summary>
    [Description("Планирование")]
    Planning = 9,

    /// <summary>
    /// Этап проверки результатов.
    /// Верификация выполненных задач.
    /// </summary>
    [Description("Тестирование")]
    Testing = 10
}