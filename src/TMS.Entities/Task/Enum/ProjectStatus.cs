namespace TMS.Entities.Task.Enum;

public enum ProjectStatus
{
    /// <summary>
    /// Черновик
    /// Проект создан, но не опубликован
    /// Доступен только владельцу
    /// Можно вносить изменения без ограничений
    /// </summary>
    Draft = 1,

    /// <summary>
    /// На рассмотрении
    /// Проект отправлен на одобрение
    /// Ожидает проверки модератором/руководством
    /// Доступ ограничен
    /// </summary>
    Pending = 2,

    /// <summary>
    /// Активен
    /// Проект запущен
    /// Доступны все функции
    /// Задачи можно создавать и выполнять
    /// </summary>
    Active = 3,

    /// <summary>
    /// Приостановлен
    /// Временная приостановка работы
    /// Задачи не создаются
    /// Существующие задачи остаются в текущем состоянии
    /// </summary>
    OnHold = 4,

    /// <summary>
    /// Завершен
    /// Все задачи выполнены
    /// Проект закрыт
    /// Доступ только для просмотра
    /// </summary>
    Completed = 5,

    /// <summary>
    /// Отменен
    /// Проект прекращен
    /// Задачи аннулированы
    /// Доступ ограничен
    /// </summary>
    Cancelled = 6,

    /// <summary>
    /// Архив
    /// Проект перемещен в архив
    /// Доступен только для истории
    /// Нет активной работы
    /// </summary>
    Archived = 7,

    /// <summary>
    /// На проверке
    /// Проект требует ревизии
    /// Возможна корректировка
    /// </summary>
    Review = 8,

    /// <summary>
    /// Планирование
    /// Этап формирования плана
    /// Определение целей и задач
    /// </summary>
    Planning = 9,

    /// <summary>
    /// Тестирование
    /// Этап проверки результатов
    /// Верификация выполненных задач
    /// </summary>
    Testing = 10
}