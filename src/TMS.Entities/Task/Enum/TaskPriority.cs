namespace TMS.Entities.Task.Enum;

public enum TaskPriority
{
    /// <summary>
    /// Низкий
    /// Минимально срочная задача
    /// Выполняется после всех остальных
    /// Не влияет на критические сроки
    /// </summary>
    Low = 1,

    /// <summary>
    /// Средний
    /// Стандартный приоритет
    /// Выполняется в плановом порядке
    /// Не требует срочного вмешательства
    /// </summary>
    Medium = 2,

    /// <summary>
    /// Высокий
    /// Требует оперативного решения
    /// Приоритет выше стандартных задач
    /// Может влиять на сроки проекта
    /// </summary>
    High = 3,

    /// <summary>
    /// Критический
    /// Требует немедленного внимания
    /// Блокирует работу других задач
    /// Может привести к серьезным последствиям
    /// </summary>
    Critical = 4,

    /// <summary>
    /// Блокирующий
    /// Блокирует работу всей системы
    /// Требует экстренного решения
    /// Критически влияет на бизнес-процессы
    /// </summary>
    Blocker = 5,

    /// <summary>
    /// Срочный
    /// Требует быстрого решения
    /// Не блокирует работу полностью
    /// Имеет жесткие дедлайны
    /// </summary>
    Urgent = 6,

    /// <summary>
    /// Немедленный
    /// Требуется немедленное выполнение
    /// Чрезвычайная ситуация
    /// Приоритет выше критического
    /// </summary>
    Immediate = 7
}