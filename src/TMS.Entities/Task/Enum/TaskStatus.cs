using System.ComponentModel;

namespace TMS.Entities.Task.Enum;

public enum TaskStatus
{
    /// <summary>
    /// Задача только создана.
    /// Ожидает назначения исполнителя.
    /// Нет активных действий.
    /// </summary>
    [Description("Новая")]
    New = 1,

    /// <summary>
    /// Исполнитель определен.
    /// Задача передана в работу.
    /// Начата оценка сложности.
    /// </summary>
    [Description("Назначена")]
    Assigned = 2,

    /// <summary>
    /// Исполнитель приступил к выполнению.
    /// Активная фаза разработки/выполнения.
    /// Регулярные обновления статуса.
    /// </summary>
    [Description("В работе")]
    InProgress = 3,

    /// <summary>
    /// Временная приостановка работы.
    /// Требуется дополнительная информация.
    /// Ожидание внешних факторов.
    /// </summary>
    [Description("Приостановлена")]
    Paused = 4,

    /// <summary>
    /// Задача выполнена.
    /// Находится на проверке у заказчика/руководителя.
    /// Возможна отправка на доработку.
    /// </summary>
    [Description("На проверке")]
    Review = 5,

    /// <summary>
    /// Все требования выполнены.
    /// Пройдена проверка.
    /// Готова к закрытию.
    /// </summary>
    [Description("Выполнена")]
    Done = 6,

    /// <summary>
    /// Задача завершена.
    /// Все документы подписаны.
    /// Архивирована.
    /// </summary>
    [Description("Закрыта")]
    Closed = 7,

    /// <summary>
    /// Задача не принята.
    /// Требуется существенная переработка.
    /// </summary>
    [Description("Отклонена")]
    Rejected = 8,

    /// <summary>
    /// Задача прекращена.
    /// Больше не актуальна.
    /// Закрыта без выполнения.
    /// </summary>
    [Description("Отменена")]
    Cancelled = 9,

    /// <summary>
    /// Временное прекращение работы.
    /// Ожидание ресурсов.
    /// Приоритет снижен.
    /// </summary>
    [Description("Отложена")]
    OnHold = 10,

    /// <summary>
    /// Ожидание ответа от клиента.
    /// Ожидание внешних зависимостей.
    /// Ожидание ресурсов.
    /// </summary>
    [Description("В ожидании")]
    Waiting = 11,

    /// <summary>
    /// Возникли непреодолимые препятствия.
    /// Требуется эскалация.
    /// Невозможно продолжить работу.
    /// </summary>
    [Description("Заблокирована")]
    Blocked = 12
}